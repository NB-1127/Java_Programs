package com.Pyramid;

public class HmPyramid2 {

	public static void main(String[] args) {
		int i,j;
		char ch = 'A';
		
        for(i=1; i<=5; i++){
        	
        
            for(j=1; j<=i; j++){
            	
            	System.out.print(j);
            
            }
            	System.out.println(ch);
            	
            		ch++;
            	               
      }   
        
             
		
	}

}
